#include<iostream>
using namespace std;

int main()
{
    int age;
    cout<<"Enter the age : ";
    cin>>age;
    if(age==10)
    {
        cout<<"You are 10";
    }
    else if (age==20)
    {
        cout<<"You are 20";
    }
    else if (age==30)
    {
        cout<<"You are 30";
    }
    else
    {
        cout<<"Wrong";
    }
    
    return 0;
}
